package practical;

public class SwitchConcept {

	public static void main(String[] args) {

		int a = 2;

		switch (a) {
		case 0:
			System.out.println("Its zero");
			break;

		case 1:
			System.out.println("Its one");
			break;

		case 2:
			System.out.println("Its two");
			break;

		default:
			System.out.println("Wrong Selection");
			break;
		}

	}

}
