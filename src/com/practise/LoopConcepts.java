package com.practise;

public class LoopConcepts {

	public static void main(String[] args) {

		// 1. For Loop
		for (int i = 0; i <= 5; i++) {
			System.out.println(i);
		}

		System.out.println("*************************");

		//2. While Loop
		int j = 1;
		while (j < 10) {
			System.out.println(j);
			j++;
		}

		System.out.println("*************************");
		
		//3. Do While Loop
		int k = 5;
		do {
			System.out.println(k);
			k++;
		} while (k <= 10);
		
		System.out.println("*************************");

	}

}
