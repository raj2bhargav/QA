package com.practise;

public class DataTypesConcept {

	public static void main(String[] args) {
		
		//1. Integer Data Types
		int a = 10;
		int b = 20;
		System.out.println(a+b);
		System.out.println("--------------------");
		
		//2. Double Data Types
		double d1 = 10.50;
		double d2 = 12.34;
		System.out.println(d1 + d2);
		System.out.println("--------------------");
		
		//3. Char Data Types
		char c1 = 'A';
		char c2 = '$';
		System.out.println(c1);
		System.out.println(c2);
		System.out.println("--------------------");
		
		//4. Boolean Data Types
		boolean b1 = true;
		boolean b2 = false;
		System.out.println(b1);
		System.out.println(b2);
		System.out.println("--------------------");
		
		//5. String is a class and not data types.
		String s1 = "AAAA";
		String s2 = "1111";
		System.out.println(s1);
		System.out.println(s2);
		System.out.println("--------------------");
		
		//6. Object
		Object obj1 = "Selenium";
		Object obj2 = 500;
		System.out.println(obj1);
		System.out.println(obj2);
		System.out.println("--------------------");

	}

}
