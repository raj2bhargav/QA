package JavaBasics;

public class LoopConcept {
	
	public static void main(String[] args) {
		
		//1. While Loop
		int i = 0;
		while(i<=10) {
			System.out.println(i);
			i++;
		}
		
		System.out.println("************************");
		
		//2. For Loop
		for (int j = 0; j < 10; j++) {
			System.out.println(j);
		}
		System.out.println("------------------------");
		
	}

}
